/*
	▶ 정규 표현식(Regular Expression)
	
		▷ . : 임의의 한 문자
		▷ * : 0개 이상의 문자
		▷ + : 1개 이상의 문자
		▷ ? : 0개 또는 1개의 문자
		▷ [] : 괄호 안에 포함된 문자 중 하나와 일치
		▷ [^] : 괄호 안에 포함된 모든 문자와 불일치
		▷ () : 괄호 안의 문자열을 한 문자로 인식
		▷ | : 두 패턴 중 하나와 일치
		▷ {n} : n개의 문자
		▷ {n,m} : 최소 n개, 최대 m개의 문자
		▷ {n,} : n개 이상의 문자
		▷ ^ : 문자열의 시작
		▷ $ : 문자열의 종료
		▷ \d : [0-9]
		▷ \w : [A-Za-z0-9_]
		▷ \s : 스페이스, 탭, 엔터 등의 공백 문자
		▷ \D : [^0-9]
		▷ \W : [^A-Za-z0-9_]
		▷ \S : 공백 문자가 아닌 문자
*/
